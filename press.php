    <div class="features"><h1>About Mixxx</h1>
    <p>Mixxx was originally created in 2002 as part of a PhD thesis on new interfaces for DJing by Tue Haste Andersen. 
    After releasing the project as open source, dozens of contributors began modifying and improving Mixxx.</p>
    <p>In 2006, a new development team lead by Adam Davison and Albert Santoni began reorganizing the project to ensure smooth growth of both the codebase
       and the number of contributors. A renewed focus on usability and stability has helped Mixxx grow to become the
       most popular free DJ software in the world, receiving <b>over 1,000,000 downloads annually</b>. Our committed team
       has worked hard to create great DJ software, and this growth is a sign of our success.</p>
       
       <p>As our user community grows, so
       does our development team - As of Mixxx 1.8.0, <b>over 86 developers and artists</b> have helped create Mixxx!
       </p>
       <p>Mixxx also participated in <a href="http://code.google.com/soc/">Google Summer of Code</a> in 2007, 2008, and 2010. Google Summer of Code is a program
          that provides college students with a stipend over the summer to work on open source projects. Mentors
          from Mixxx introduced students to open source software development and helped them gain experience
          working with multithreaded, real-time multimedia software.
    <h1>Why?</h1>
    <p>Mixxx started as an open source project because of demand for DJing software on Linux, and discontent
        with proprietary DJ software on Windows and Mac OS X. Today, Mixxx development is driven by
        the simple idea that together we can create a better way to DJ, and that has brought Mixxx to the cutting
        edge. Mixxx is the only free cross-platform vinyl control software and has the most advanced MIDI controller
        support via our innovative Javascript-based MIDI scripting engine. We are continuing to pursue new and exciting 
        features that give DJs more tools to create better live mixes.
    </p>
        
    <p>Besides scratching our own itches,
        many people work on Mixxx as a way to learn about audio programming, or even just because it's fun and
        they enjoy programming. The creative 
        aspect of Mixxx attracts developers with a broad range of skills and talents, and that makes for a fun
        development team that helps Mixxx continue to grow.</p> 
        
    <h1>Interviews</h1>
    <p>Please contact our project manager, <a href="mailto:adamd@mixxx.org">Adam Davison</a> for interviews. We won't bite, we promise.</p>
    
